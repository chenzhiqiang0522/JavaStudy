/**
 * 比较运算法
 *      >  <  >=  <=  !=  ==
 * */
public class Cal03 {
    public static void main(String[] args) {
        System.out.println(2 < 3);// false
        System.out.println(2 > 3);// true
        System.out.println(2 >= 3);// true
        System.out.println(2 <= 3);// false
        System.out.println(2 != 3);// true
        System.out.println(2 == 3);// false

    }
}
